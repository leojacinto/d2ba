#!/usr/bin/env python3
"""
scaffold.py — create a DUMMY ServiceNow Fluent app scaffold, then merge a
docx_to_build_agent.py package into it as a "sidecar":

  - the .md spec goes to <target>/GUIDE_<slug>.md (project root), matching
    ServiceNow's own documented Build Agent grounding-file convention
    ("Place a markdown file, for example named GUIDE_*.md or
    BUILD_AGENT_RULES.md, in your project directory. Build Agent reads these
    grounding files and follows your conventions throughout the session.")
  - images/ and embedded-data/ go to <target>/docs/, with the guide file's
    relative links rewritten to match

DISCLAIMER: this scaffold is hand-written to mirror the file structure that
`npx @servicenow/sdk init --template typescript.basic` produces on
@servicenow/sdk 4.8.1 — it does NOT call `now-sdk init` and never contacts a
ServiceNow instance. The scopeId in now.config.json is a locally-generated
random value, not one registered against any instance. Why: `now-sdk init`
always tries to log into an instance to register the scope (confirmed by
testing — it does this even with no --auth flag given, falling back to
whatever alias is marked default), and this scaffold is meant to be a dummy
placeholder for Build Agent to read from, not a real app registration.

What's still genuine: `now-sdk build` (via `npm run build` / verify_build())
is the real SDK compiling the real Fluent files — confirmed by testing that
it succeeds with zero auth configured and zero instance contact. Only
`init`'s scope-registration step needed an instance; `build`'s verification
does not. So this scaffold is unregistered but still genuinely SDK-verified
as buildable, not hand-approximated in the sense that matters.

Only the typescript.basic template is supported by this hand-written path —
its exact file contents were captured from a real `now-sdk init` run and are
reproduced here verbatim as boilerplate. Other templates would need their
own captured boilerplate to support the same way.
"""
